public class PasswordValidation {

    private String password;

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    @Override
    public String toString() {
        return "PasswordValidation{" +
                "password='" + password + '\'' +
                '}';
    }
}
